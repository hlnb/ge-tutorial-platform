function inferSectionId(slug) {
  const matchingSection = sections.find(
    (section) => slug === section.slug || slug.startsWith(`${section.slug}/`),
  );

  return matchingSection?.id || null;
}

function buildProjectGrouping(tutorial) {
  if (!tutorial.project) {
    return null;
  }

  return {
    id: tutorial.project,
    part: tutorial.projectPart,
    title: tutorial.projectTitle,
  };
}

